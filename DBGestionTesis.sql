create procedure spuListarExpedientesParaRevisores
as
begin
		select A.CodExpediente AS Codigo_Expediente,A.CodTesis AS Codigo_de_Tesis,B.Titulo as Titulo,A.Estado,C.Nombre as Tramite
		from TExpediente A inner join TTesis B on A.CodTesis=B.CodTesis
							inner join TTipoTramite C ON A.CodTipoTramite=C.CodTipoTramite
		where A.Estado='PROCESO' and A.CodTipoTramite='TR000001'
end
