create procedure spuListarAsignaciones
as
begin
	select distinct *--EA.CodExpedienteAsignacion as 'Código Asignación',T.CodTesis as 'Código Tesis', T.Titulo,TD.Nombres,TD.Apellidos
					from TExpedienteAsignacion EA  left join TExpedienteAsig_Docente EAD ON EA.CodExpedienteAsignacion = EAD.CodExpedienteAsignacion 
					inner join TExpediente E ON EA.CodExpediente = E.CodExpediente
					inner join TTesis T ON E.CodTesis = T.CodTesis 
					inner join TTesista_Tesis TT ON T.CodTesis = TT.CodTesis 
					inner join TTesista TE ON TT.CodAlumno = TE.CodAlumno
					inner join TDocente TD ON EAD.CodDocente = TD.CodDocente
end

