/* Crear las tablas*/
-------------------------------------------------------------------
create table TTesista
(
    CodAlumno       varchar(10) check (CodAlumno like '[0-9][0-9][0-9][0-9][0-9][0-9]'),
	Nombres          varchar(80) NOT NULL,
	Apellidos       varchar(100) NOT NULL,
	TipoAlumno      varchar(80) check (TipoAlumno in ('REGULAR','BACHILLER')),
	EscuelaProfesional    varchar(80) check (EscuelaProfesional in ('INGENIERIA INFORMATICA Y DE SISTEMAS')),
	
	-- definición de claves
    PRIMARY KEY (CodAlumno)
)
go
---------------------------------------------------------------------
create table TDocente
(
    CodDocente      varchar(10) check (CodDocente like '[0-9][0-9][0-9][0-9][0-9][0-9]'),
	Nombres          varchar(30) NOT NULL,
	Apellidos       varchar(100) NOT NULL,
	Tipo           varchar(40) check (Tipo in ('CONTRATADO','NOMBRADO')),
	DepartamentoAcademico      varchar(150) check (DepartamentoAcademico in ('INGENIERIA INFORMATICA','INGENIERIA MECANICA','INGENIERIA ELECTRICA','INGENIERIA ELECTRONICA')),
	-- definición de claves
    PRIMARY KEY (CodDocente)
)
go
--------------------------------------------------------------------
create table TUsuarioAdministrativo
(
    CodUsuario      varchar(10) check (CodUsuario like '[U][0-9][0-9][0-9][0-9][0-9][0-9]'),
	Nombres          varchar(50) NOT NULL,
	Apellidos       varchar(100) NOT NULL,
	CargoAdministrativo       varchar(20) NOT NULL,
	Contraseña		varchar(10),
	-- definición de claves
    PRIMARY KEY (CodUsuario)
	
)
go
---------------------------------------------------------------------
create table TTesis
(
    CodTesis        varchar(10) check (CodTesis like '[T][0-9][0-9][0-9][0-9][0-9][0-9]'),
	Titulo          varchar(200) NOT NULL,
	CodDocente      varchar(10),
	-- definición de claves
    PRIMARY KEY (CodTesis),
	FOREIGN KEY (CodDocente) references TDocente,
)
go
-------------------------------------------------------------------
create table TTesista_Tesis
(
	CodAlumno  varchar(10),
	CodTesis   varchar(10),
	-- definición de claves
    PRIMARY KEY (CodAlumno,CodTesis),
	FOREIGN KEY (CodAlumno) references TTesista,
	FOREIGN KEY (CodTesis) references TTesis,
)
go
-------------------------------------------------------------------
create table TTipoTramite
(
    CodTipoTramite   varchar(10) check (CodTipoTramite like '[T][R][0-9][0-9][0-9][0-9][0-9][0-9]'),
	Nombre           varchar(100) NOT NULL,
	-- definición de claves
    PRIMARY KEY (CodTipoTramite),
)
go
-------------------------------------------------------------------
create table TRequisito
(
    CodRequisito     varchar(10) check (CodRequisito like '[R][E][Q][0-9][0-9][0-9][0-9][0-9][0-9]'),
	Nombre           varchar(100) NOT NULL,
	-- definición de claves
    PRIMARY KEY (CodRequisito),
)
go
create table TTipoTramite_Requisitos
(
	CodRequisito varchar(10),
	CodTipoTramite  varchar(10),
	-- definición de claves
    PRIMARY KEY (CodTipoTramite,CodRequisito),
	FOREIGN KEY (CodTipoTramite) references TTipoTramite,
	FOREIGN KEY (CodRequisito) references TRequisito,
)
go
-------------------------------------------------------------------
create table TExpediente
(
    CodExpediente   varchar(10) check (CodExpediente like '[E][0-9][0-9][0-9][0-9][0-9][0-9]'),
	Fecha           varchar(100) NOT NULL,
	Estado          varchar(100) NOT NULL,
	CodTesis        varchar(10),
	CodTipoTramite  varchar(10),
	-- definición de claves
    PRIMARY KEY (CodExpediente),
	FOREIGN KEY (CodTipoTramite) references TTipoTramite,
	FOREIGN KEY (CodTesis) references TTesis,
)
go

-------------------------------------------------------------------


-------------------------------------------------------------------
create table TExpedienteAsignacion	
(
    CodExpedienteAsignacion	           varchar(10) check (CodExpedienteAsignacion like '[E][A][0-9][0-9][0-9][0-9][0-9][0-9]'),
	Fecha                              varchar(100) NOT NULL,
	CodExpediente                      varchar(10),
	-- definición de claves
    PRIMARY KEY (CodExpedienteAsignacion),
	FOREIGN KEY (CodExpediente) REFERENCES TExpediente,
)
go

-------------------------------------------------------------------
create table TExpedienteAsig_Docente	
(
	CodExpedienteAsignacion             varchar(10),
	CodDocente                          varchar(10),
	Rol                                 varchar(100) NOT NULL,
	-- definición de claves
    PRIMARY KEY (CodExpedienteAsignacion,CodDocente),
	FOREIGN KEY (CodExpedienteAsignacion) REFERENCES TExpedienteAsignacion,
	FOREIGN KEY (CodDocente) REFERENCES TDocente,
)
go

-------------------------------------------------------------------

create table TResolucionJurado
(
    CodResolucionJurado	   varchar(10) check (CodResolucionJurado like '[R][J][0-9][0-9][0-9][0-9][0-9][0-9]'),
	FechaResolucion                   varchar(100) NOT NULL,
	Lugar                             varchar(100),
	FechaSustentacion                 varchar(100),
	HoraSustentacion                  varchar(100),
	CodExpediente                      varchar(10),
	-- definición de claves
    PRIMARY KEY (CodResolucionJurado),
	FOREIGN KEY (CodExpediente) REFERENCES TExpediente,
)
go

-------------------------------------------------------------------
create table TExpedienteAsig_DocenteJurado	
(
	CodResolucionJurado       varchar(10),
	CodDocente                          varchar(10),
	Rol                                 varchar(100),
	-- definición de claves
    PRIMARY KEY (CodResolucionJurado ,CodDocente),
	FOREIGN KEY (CodResolucionJurado ) REFERENCES TResolucionJurado ,
	FOREIGN KEY (CodDocente) REFERENCES TDocente,
)
go
-------------------------------------------------------------------
create table TActaSustentacion
(
	CodActaSustentacion                 varchar(10) check (CodActaSustentacion like '[A][0-9][0-9][0-9][0-9][0-9][0-9]'),
	FechaSustentacion                   varchar(40) NOT NULL,
	HoraDeInicio                        varchar(100) NOT NULL,
	HoraFinalizacion                    varchar(100) NOT NULL,
	NotaNro                             varchar(20) NOT NULL,
	NotaFinal                           varchar(20) NOT NULL,
	CodExpediente                       varchar(10),

	-- definición de claves
    PRIMARY KEY (CodActaSustentacion),
	FOREIGN KEY (CodExpediente) REFERENCES TExpediente,
)
go
-------------------------------------------------------------------
-------------------------------------------------------------------

create table TInformeEvaluacion
(
    CodInformeEvaluacion           varchar(10) check (CodInformeEvaluacion like '[I][E][0-9][0-9][0-9][0-9][0-9][0-9]'),
	Tipo                           varchar(20) NOT NULL,
	Fecha                          varchar(50) NOT NULL,
	Observaciones                  varchar(100) NOT NULL,
	CodDocente                     varchar(10),
	NotaNro                        varchar(20) NOT NULL,
	NotaText                       varchar(20) NOT NULL,
	CodExpediente                  varchar(10),
	-- definición de claves
    PRIMARY KEY (CodInformeEvaluacion),
	FOREIGN KEY (CodDocente) REFERENCES TDocente,
	FOREIGN KEY (CodExpediente) REFERENCES TExpediente,
)
go

-------------------------------------------------------------------
create table TResolucion	
(
    CodResolucion               varchar(10) check (CodResolucion like '[R][0-9][0-9][0-9][0-9][0-9][0-9]'),
	Tipo                  varchar(100) NOT NULL,
	Fecha                 varchar(40) NOT NULL,
	NotaFinal             varchar(20) NOT NULL,
	CodExpediente        varchar(10),
	-- definición de claves
    PRIMARY KEY (CodResolucion),
	FOREIGN KEY (CodExpediente) REFERENCES TExpediente,
)
go


