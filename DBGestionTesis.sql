create procedure spu_MostrarSeguimiento(@CodTesis varchar (10))
as
begin
select distinct A.CodExpediente as CodigoExpediente,A.Fecha as FechaInscripcion,B.Titulo,D.CodAlumno AS CodigoAlumno,D.Nombres,D.Apellidos,A.Estado
		from TExpediente A inner join TTesis B on A.CodTesis=B.CodTesis
							INNER JOIN TTesista_Tesis C on B.CodTesis=C.CodTesis
							inner join TTesista D on C.CodAlumno=D.CodAlumno
							LEFT join TInformeEvaluacion E on A.CodExpediente=E.CodExpediente
		where A.CodTipoTramite='TR000001' and B.CodTesis=@CodTesis
end
go

